<script type="text/javascript">
var cal_cnt	= 0;
function prev_m_cal()
{
	if (cal_cnt == 0)
	{
		alert("이벤트 일정이 없습니다.");
	}else if (cal_cnt == 1){
		$("#list_cal6").fadeOut("fast", function(){
			$("#list_cal5").fadeIn("slow");
			$(".m_num").html("5월");
		});
		cal_cnt = 0;
	}else if (cal_cnt == 2){
		$("#list_cal7").fadeOut("fast", function(){
			$("#list_cal6").fadeIn("slow");
			$(".m_num").html("6월");
		});
		cal_cnt = 1;
	}
}

function next_m_cal()
{
	if (cal_cnt == 0)
	{
		$("#list_cal5").fadeOut("fast", function(){
			$("#list_cal6").fadeIn("slow");
			$(".m_num").html("6월");
		});
		cal_cnt = 1;
	}else if (cal_cnt == 1){
		$("#list_cal6").fadeOut("fast", function(){
			$("#list_cal7").fadeIn("slow");
			$(".m_num").html("7월");
		});
		cal_cnt = 2;
	}else if (cal_cnt == 2){
		alert("이벤트 일정이 없습니다.");
	}
}
</script>